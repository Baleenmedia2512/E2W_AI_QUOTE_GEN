import React, { useState, useRef, useEffect } from 'react';
import {
  IonCard,
  IonCardHeader,
  IonCardTitle,
  IonCardContent,
  IonButton,
  IonInput,
  IonSpinner,
  IonChip,
  IonIcon,
} from '@ionic/react';
import { sendSharp, refreshSharp } from 'ionicons/icons';
import { useHistory } from 'react-router-dom';
import { useAppStore } from '../../store';
import { sendMessageToGemini } from '../../services/geminiService';
import { Message } from '../../types/chat';
import { Quote, QuoteItem } from '../../types/quote';
import { saveChatHistory, loadChatHistory } from '../../utils/localStorage';
import { SAMPLE_PROMPTS } from '../../utils/promptTemplates';
import './ChatInterface.css';

const ChatInterface: React.FC = () => {
  const history = useHistory();
  const { proposal, setCurrentQuote } = useAppStore();
  const [messages, setMessages] = useState<Message[]>([]);
  const [inputValue, setInputValue] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  // const [detectedQuoteData, setDetectedQuoteData] = useState<any>(null);
  const messagesEndRef = useRef<HTMLDivElement>(null);
  const inputRef = useRef<HTMLIonInputElement>(null);

  // Debug: Log proposal state when it changes
  useEffect(() => {
    console.log('Proposal state updated:', {
      hasFile: !!proposal?.file,
      fileName: proposal?.fileName,
      hasTextContent: !!proposal?.textContent,
      textContentLength: proposal?.textContent?.length || 0,
      pageCount: proposal?.pageCount
    });
  }, [proposal]);

  // Load chat history on mount
  useEffect(() => {
    const history = loadChatHistory();
    if (history && history.length > 0) {
      setMessages(history.map(msg => ({
        ...msg,
        timestamp: new Date(msg.timestamp),
      })));
    }
  }, []);

  // Save chat history when messages change
  useEffect(() => {
    if (messages.length > 0) {
      saveChatHistory(messages);
    }
  }, [messages]);

  // Scroll to bottom when messages change
  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  }, [messages, isLoading]);

  const handleSendMessage = async () => {
    if (!inputValue.trim()) return;

    console.log('Sending message with proposal context:', {
      hasProposal: !!proposal,
      hasTextContent: !!proposal?.textContent,
      textContentLength: proposal?.textContent?.length || 0
    });

    const userMessage: Message = {
      id: Date.now().toString(),
      role: 'user',
      content: inputValue.trim(),
      timestamp: new Date(),
    };

    setMessages(prev => [...prev, userMessage]);
    setInputValue('');
    setIsLoading(true);
    setError(null);

    try {
      console.log('Calling Gemini API...');
      const response = await sendMessageToGemini({
        userMessage: userMessage.content,
        proposalText: proposal.textContent,
        chatHistory: messages,
      });

      console.log('Gemini response received:', response);

      const assistantMessage: Message = {
        id: (Date.now() + 1).toString(),
        role: 'assistant',
        content: response.message,
        timestamp: new Date(),
      };

      setMessages(prev => [...prev, assistantMessage]);

      // If complete quote was generated by AI, store it and AUTO-NAVIGATE
      if (response.isQuoteGeneration && response.quoteData) {
        console.log('✓ Complete quote generated by AI:', response.quoteData);
        // setDetectedQuoteData(response.quoteData);
        
        // Convert AI-generated quote data to Quote object
        const quoteItems: QuoteItem[] = response.quoteData.items.map((section: any, index: number) => {
          const lineItems = section.lineItems.map((item: any, lineIndex: number) => ({
            id: `${index}-${lineIndex}`,
            description: item.description,
            quantity: item.quantity || 1,
            unitPrice: item.unitPrice || 0,
            total: (item.quantity || 1) * (item.unitPrice || 0)
          }));

          const sectionSubtotal = lineItems.reduce((sum: number, item: any) => sum + item.total, 0);

          return {
            id: index.toString(),
            title: section.title,
            description: section.title,
            quantity: 1,
            rate: sectionSubtotal,
            total: sectionSubtotal,
            lineItems,
            subtotal: sectionSubtotal
          };
        });

        const subtotal = quoteItems.reduce((sum, item) => sum + item.total, 0);
        const gstPercentage = 18;
        const gstAmount = subtotal * (gstPercentage / 100);
        
        const quote: Quote = {
          id: Date.now().toString(),
          quoteNumber: `QT-${Date.now().toString().slice(-6)}`,
          date: new Date().toISOString(),
          validUntil: new Date(Date.now() + 30 * 24 * 60 * 60 * 1000).toISOString(),
          items: quoteItems,
          subtotal,
          gstEnabled: true,
          gstPercentage,
          gstAmount,
          total: subtotal + gstAmount,
          deliveryTimeline: response.quoteData.deliveryTimeline || '7 working days after payment',
          termsAndConditions: response.quoteData.termsAndConditions || 'Standard terms and conditions apply',
          notes: response.quoteData.notes,
          createdAt: new Date(),
          updatedAt: new Date()
        };
        
        console.log('✓ Auto-creating quote from AI data:', quote);
        setCurrentQuote(quote);
        
        // Show success message before navigating
        const quoteReadyMessage: Message = {
          id: (Date.now() + 2).toString(),
          role: 'assistant',
          content: '✓ Quote generated successfully! Redirecting to quote preview...',
          timestamp: new Date(),
        };
        setMessages(prev => [...prev, quoteReadyMessage]);
        
        // Auto-navigate to quote page after a brief delay
        setTimeout(() => {
          console.log('🚀 Auto-navigating to /quote');
          history.push('/quote');
        }, 1500);
      }
    } catch (err: any) {
      console.error('Chat error:', err);
      setError(err.message || 'Failed to send message');
      const errorMessage: Message = {
        id: (Date.now() + 1).toString(),
        role: 'assistant',
        content: err.message || 'Sorry, I encountered an error. Please try again.',
        timestamp: new Date(),
        isError: true,
      };
      setMessages(prev => [...prev, errorMessage]);
    } finally {
      setIsLoading(false);
    }
  };

  const handleKeyPress = (e: any) => {
    if (e.key === 'Enter' && !e.shiftKey) {
      e.preventDefault();
      handleSendMessage();
    }
  };

  const handleSamplePrompt = (prompt: string) => {
    setInputValue(prompt);
    inputRef.current?.setFocus();
  };

  const handleClearChat = () => {
    setMessages([]);
    setError(null);
    // setDetectedQuoteData(null);
    saveChatHistory([]);
  };

  return (
    <IonCard className="chat-interface">
      <IonCardHeader>
        <div className="chat-header">
          <IonCardTitle>AI Assistant</IonCardTitle>
          {messages.length > 0 && (
            <IonButton fill="clear" size="small" onClick={handleClearChat}>
              <IonIcon icon={refreshSharp} slot="icon-only" />
            </IonButton>
          )}
        </div>
      </IonCardHeader>
      <IonCardContent className="chat-content">
        {/* Sample Prompts */}
        {messages.length === 0 && (
          <div className="sample-prompts">
            <p className="sample-prompts-title">Try asking:</p>
            <div className="sample-prompts-chips">
              {SAMPLE_PROMPTS.map((prompt, index) => (
                <IonChip
                  key={index}
                  onClick={() => handleSamplePrompt(prompt)}
                  className="sample-prompt-chip"
                >
                  {prompt}
                </IonChip>
              ))}
            </div>
          </div>
        )}

        {/* Messages */}
        <div className="messages-container">
          {messages.map(message => (
            <div
              key={message.id}
              className={`message ${message.role} ${message.isError ? 'error' : ''}`}
            >
              <div className="message-bubble">
                <div className="message-content">{message.content}</div>
                <div className="message-timestamp">
                  {message.timestamp.toLocaleTimeString([], {
                    hour: '2-digit',
                    minute: '2-digit',
                  })}
                </div>
              </div>
            </div>
          ))}

          {/* Typing Indicator */}
          {isLoading && (
            <div className="message assistant">
              <div className="message-bubble typing-indicator">
                <IonSpinner name="dots" />
              </div>
            </div>
          )}

          <div ref={messagesEndRef} />
        </div>

        {/* Error Message */}
        {error && !isLoading && (
          <div className="error-banner">
            <p>{error}</p>
          </div>
        )}

        {/* Input Area */}
        <div className="input-container">
          <IonInput
            ref={inputRef}
            value={inputValue}
            onIonInput={(e) => setInputValue(e.detail.value || '')}
            onKeyPress={handleKeyPress}
            placeholder={
              proposal.textContent
                ? 'Ask about the proposal or request a quote...'
                : 'Upload a proposal to start chatting...'
            }
            disabled={isLoading || !proposal.textContent}
            className="chat-input"
          />
          <IonButton
            onClick={handleSendMessage}
            disabled={!inputValue.trim() || isLoading || !proposal.textContent}
            className="send-button"
          >
            <IonIcon icon={sendSharp} slot="icon-only" />
          </IonButton>
        </div>
      </IonCardContent>
    </IonCard>
  );
};

export default ChatInterface;
